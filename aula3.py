"""
Python = Linguagem de programação.
Tipo de tipagem = Dinâmica / Forte.
str -> string -> texto
String são textos que estão dentro de aspas
"""
print(123)

# Aspas simles
print('Félix Panzo')
print(1, 'Félix "Panzo"')

# Aspas duplas
print("Félix Panzo")
# Escape
print("Félix \"Panzo\"")

# r
print(r"Félix \"Panzo\"")

