# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer núnero
# Positivo ou negativo. int sem sinal é considerado positivo
#print(11) # int
#print(-11) # int
#print(0) # int
# float -> Número com ponto flutuante
# O tipo float representa qualquer qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positvo.
#print(1.1, 10.11) # float
#print(0.0, -1.5) # float

# A função type mostra o tipo que o python
# inferiu o valor
print(type('Panzo'))
